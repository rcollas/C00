#include <unistd.h>

void	ft_putnbr(int nb)
{
	unsigned int abs_nb;

	abs_nb = nb;
	if (nb < 0)
		write(1, "-", 1);
	if (abs_nb >= 10)
		ft_putnbr(abs_nb / 10);
	abs_nb = abs_nb % 10 + 48;
	write(1, &abs_nb, 1);
}

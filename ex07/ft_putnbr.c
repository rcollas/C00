#include <unistd.h>

void	ft_putnbr(int nb)
{
	int tableau[9];
	int i;

	i = 0;
	while (nb > 0)
	{
		tableau[i] = nb - (nb / 10) * 10 + 48;
		i++;
		nb /= 10;
	}
	i--;
	while (i >= 0)
	{
		write(1, &tableau[i], 1);
		i--;
	}
}
